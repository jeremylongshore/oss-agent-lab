class SpecialistMetadata:
    """Parsed metadata from a specialist's SKILL.md frontmatter."""

    def __init__(self, data: dict[str, Any], specialist_dir: Path) -> None:
        self.name: str = data.get("name", specialist_dir.name)
        self.display_name: str = data.get("display_name", self.name.replace("_", " ").title())
        self.description: str = data.get("description", "No description provided.")
        self.version: str = data.get("version", "0.1.0")
        self.source_repo: str = data.get("source_repo", "unknown/unknown")
        self.license: str = data.get("license", "MIT")
        self.tier: str = data.get("tier", "experimental")
        self.capabilities: list[str] = data.get("capabilities", [])
        self.allowed_tools: list[str] = data.get("allowed_tools", [])
        self.output_formats: list[str] = data.get("output_formats", [])

        # Derived helpers used in templates
        self.class_name: str = _to_class_name(self.name)
        self.module_path: str = f"agents.specialists.{self.name}.agent"
        self.slug: str = self.name  # already snake_case


def _to_class_name(name: str) -> str:
    """Convert a snake_case name to a PascalCase class name."""
    return "".join(word.capitalize() for word in re.split(r"[_\-]", name)) + "Specialist"


def parse_skill_md(skill_md_path: Path) -> SpecialistMetadata:
    """Parse YAML frontmatter from a SKILL.md file.

    Args:
        skill_md_path: Path to the SKILL.md file.

    Returns:
        SpecialistMetadata populated from the frontmatter.

    Raises:
        FileNotFoundError: If the file does not exist.
        ValueError: If frontmatter is missing or malformed.
    """
    if not skill_md_path.exists():
        raise FileNotFoundError(f"SKILL.md not found: {skill_md_path}")

    raw = skill_md_path.read_text(encoding="utf-8")
    match = re.match(r"^---\n(.*?)\n---", raw, re.DOTALL)
    if not match:
        raise ValueError(f"No YAML frontmatter found in {skill_md_path}")

    data: dict[str, Any] = yaml.safe_load(match.group(1)) or {}
    return SpecialistMetadata(data, skill_md_path.parent)


