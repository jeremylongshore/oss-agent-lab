_FORMAT_SUBDIRS = {
    "python_api": "python_api",
    "cli": "cli",
    "mcp_server": "mcp",
    "agent_skill": "skills",
    "rest_api": "rest",
}

_FORMAT_SUFFIXES = {
    "python_api": "",
    "cli": "_cli",
    "mcp_server": "_mcp",
    "agent_skill": "_skill",
    "rest_api": "_rest",
}

_FORMAT_GENERATORS = {
    "python_api": _python_api_template,
    "cli": _cli_template,
    "mcp_server": _mcp_template,
    "agent_skill": _skill_template,
    "rest_api": _rest_template,
}


def generate_outputs(
    specialist_dir: Path,
    output_dir: Path | None = None,
) -> dict[str, Path]:
    """Generate all output formats for a specialist.

    Parses SKILL.md frontmatter, then writes one file per output format into
    the appropriate subdirectory of ``output_dir``.

    Args:
        specialist_dir: Path to the specialist directory containing agent.py
                        and SKILL.md.
        output_dir: Root directory for generated files. Defaults to an
                    ``output/`` sibling of the project root inferred from
                    specialist_dir.

    Returns:
        Dict mapping format name (e.g. "python_api") to the generated Path.

    Raises:
        FileNotFoundError: If SKILL.md does not exist in specialist_dir.
        ValueError: If SKILL.md has no valid YAML frontmatter.
    """
    specialist_dir = specialist_dir.resolve()
    skill_md = specialist_dir / "SKILL.md"
    meta = parse_skill_md(skill_md)

    # Resolve output root: default to <project_root>/output
    if output_dir is None:
        # Walk up from specialist_dir looking for pyproject.toml as project root.
        project_root = _find_project_root(specialist_dir) or specialist_dir.parents[2]
        output_dir = project_root / "output"

    generated: dict[str, Path] = {}

    for fmt_key, generator in _FORMAT_GENERATORS.items():
        subdir_name = _FORMAT_SUBDIRS[fmt_key]
        suffix = _FORMAT_SUFFIXES[fmt_key]
        target_dir = output_dir / subdir_name
        target_dir.mkdir(parents=True, exist_ok=True)

        filename = f"{meta.slug}{suffix}.py"
        target_path = target_dir / filename
        content = generator(meta)
        target_path.write_text(content, encoding="utf-8")
        generated[fmt_key] = target_path

    return generated


def _find_project_root(start: Path) -> Path | None:
    """Walk up the directory tree looking for pyproject.toml."""
    current = start
    for _ in range(10):
        if (current / "pyproject.toml").exists():
            return current
        parent = current.parent
        if parent == current:
            break
        current = parent
    return None


