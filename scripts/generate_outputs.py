def _mcp_template(meta: SpecialistMetadata) -> str:
    """Generate an MCP JSON-RPC server stub."""
    tool_registrations = "\n".join(
        f'''\
    {{
        "name": "{tool}",
        "description": "TODO: describe the {tool} tool",
        "inputSchema": {{
            "type": "object",
            "properties": {{
                "query": {{"type": "string", "description": "The input query"}},
            }},
            "required": ["query"],
        }},
    }},'''
        for tool in meta.allowed_tools
    )
    return f'''\
#!/usr/bin/env python3
"""MCP (Model Context Protocol) JSON-RPC server stub for {meta.display_name}.

Auto-generated by generate_outputs.py — edit to taste.

This stub implements the MCP wire protocol over stdio. Each tool defined in
SKILL.md is registered and dispatched to the underlying specialist.

Usage:
    python {meta.slug}_mcp.py

Protocol: https://modelcontextprotocol.io/specification
"""
from __future__ import annotations

import asyncio
import json
import logging
import sys
from typing import Any

# TODO: Adjust import path to match your project layout.
from {meta.module_path} import {meta.class_name}
from oss_agent_lab.contracts import Intent, Query, SpecialistRequest

logger = logging.getLogger("{meta.name}_mcp")

# ---------------------------------------------------------------------------
# Tool registry
# ---------------------------------------------------------------------------

TOOLS: list[dict[str, Any]] = [
{tool_registrations}
]

SPECIALIST_NAME = "{meta.name}"
SPECIALIST_VERSION = "{meta.version}"

# ---------------------------------------------------------------------------
# MCP JSON-RPC handler
# ---------------------------------------------------------------------------


class MCPServer:
    """Minimal MCP server over stdio (newline-delimited JSON)."""

    def __init__(self) -> None:
        self._specialist = {meta.class_name}()

    async def handle(self, message: dict[str, Any]) -> dict[str, Any] | None:
        """Dispatch an incoming JSON-RPC message and return the response."""
        method = message.get("method", "")
        msg_id = message.get("id")

        if method == "initialize":
            return self._ok(msg_id, {{
                "protocolVersion": "2024-11-05",
                "capabilities": {{"tools": {{}}}},
                "serverInfo": {{"name": SPECIALIST_NAME, "version": SPECIALIST_VERSION}},
            }})

        if method == "tools/list":
            return self._ok(msg_id, {{"tools": TOOLS}})

        if method == "tools/call":
            return await self._call_tool(msg_id, message.get("params", {{}}))

        if method == "notifications/initialized":
            return None  # no response for notifications

        return self._error(msg_id, -32601, f"Method not found: {{method}}")

    async def _call_tool(
        self, msg_id: int | str | None, params: dict[str, Any]
    ) -> dict[str, Any]:
        tool_name: str = params.get("name", "")
        arguments: dict[str, Any] = params.get("arguments", {{}})
        query: str = arguments.get("query", "")

        if tool_name not in {{t["name"] for t in TOOLS}}:
            return self._error(msg_id, -32602, f"Unknown tool: {{tool_name}}")

        try:
            # TODO: Route to the appropriate specialist method by tool_name.
            request = SpecialistRequest(
                intent=Intent(
                    action=tool_name,
                    domain="general",
                    confidence=1.0,
                    parameters=arguments,
                ),
                query=Query(user_input=query),
                specialist_name=SPECIALIST_NAME,
            )
            response = await self._specialist.execute(request)
            content = [{{"type": "text", "text": json.dumps(response.result, indent=2)}}]
            return self._ok(msg_id, {{"content": content}})
        except Exception as exc:  # noqa: BLE001
            logger.exception("Tool execution failed")
            return self._error(msg_id, -32603, str(exc))

    @staticmethod
    def _ok(msg_id: int | str | None, result: Any) -> dict[str, Any]:
        return {{"jsonrpc": "2.0", "id": msg_id, "result": result}}

    @staticmethod
    def _error(msg_id: int | str | None, code: int, message: str) -> dict[str, Any]:
        return {{"jsonrpc": "2.0", "id": msg_id, "error": {{"code": code, "message": message}}}}


async def main() -> None:
    """Run the MCP server, reading JSON-RPC messages from stdin."""
    logging.basicConfig(level=logging.WARNING, stream=sys.stderr)
    server = MCPServer()
    loop = asyncio.get_event_loop()
    reader = asyncio.StreamReader()
    protocol = asyncio.StreamReaderProtocol(reader)
    await loop.connect_read_pipe(lambda: protocol, sys.stdin)

    writer_transport, writer_protocol = await loop.connect_write_pipe(
        asyncio.BaseProtocol, sys.stdout.buffer
    )
    writer = asyncio.StreamWriter(writer_transport, writer_protocol, reader, loop)

    while True:
        line = await reader.readline()
        if not line:
            break
        try:
            message = json.loads(line.decode())
        except json.JSONDecodeError:
            continue
        response = await server.handle(message)
        if response is not None:
            writer.write((json.dumps(response) + "\\n").encode())
            await writer.drain()


if __name__ == "__main__":
    asyncio.run(main())
'''


def _skill_template(meta: SpecialistMetadata) -> str:
    """Generate an Agent Skill package wrapper."""
    caps_list = ", ".join(f'"{c}"' for c in meta.capabilities)
    short_name = meta.class_name[: -len("Specialist")]
    return f'''\
#!/usr/bin/env python3
"""Agent Skill wrapper for {meta.display_name}.

Auto-generated by generate_outputs.py — edit to taste.

This module exposes the specialist as a reusable Skill that can be registered
with any orchestrator supporting the Agent Skill protocol (e.g. Claude's skill
runner, LangChain agent toolkits, or a custom router).

Usage:
    from {meta.slug}_skill import {short_name}Skill

    skill = {short_name}Skill()
    result = await skill.execute({{"query": "your query here"}})
"""
from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any

# TODO: Adjust import path to match your project layout.
from {meta.module_path} import {meta.class_name}
from oss_agent_lab.contracts import Intent, Query, SpecialistRequest


@dataclass
class SkillManifest:
    """Declarative metadata describing this skill to an orchestrator."""

    name: str = "{meta.name}"
    display_name: str = "{meta.display_name}"
    description: str = "{meta.description}"
    version: str = "{meta.version}"
    capabilities: list[str] = field(default_factory=lambda: [{caps_list}])
    # TODO: Add parameter schema in JSON Schema format for validation.
    input_schema: dict[str, Any] = field(default_factory=lambda: {{
        "type": "object",
        "properties": {{
            "query": {{"type": "string", "description": "The user query or topic."}},
            "action": {{"type": "string", "description": "The intent action to perform."}},
            "domain": {{"type": "string", "description": "The intent domain."}},
            "parameters": {{"type": "object", "description": "Extra specialist parameters."}},
        }},
        "required": ["query"],
    }})


class {short_name}Skill:
    """{meta.display_name} packaged as a reusable Agent Skill.

    Register this with your orchestrator to expose {meta.name} capabilities
    through a standard skill interface.
    """

    manifest: SkillManifest = SkillManifest()

    def __init__(self) -> None:
        self._specialist = {meta.class_name}()

    async def execute(self, inputs: dict[str, Any]) -> dict[str, Any]:
        """Execute the skill with the given inputs.

        Args:
            inputs: Dict conforming to SkillManifest.input_schema. Must contain
                    at minimum a "query" key.

        Returns:
            Dict containing the specialist result under key "result", plus
            "status" and "specialist_name".
        """
        query: str = inputs["query"]
        action: str = inputs.get("action", "{meta.capabilities[0] if meta.capabilities else "run"}")
        domain: str = inputs.get("domain", "general")
        parameters: dict[str, Any] = inputs.get("parameters", {{}})

        request = SpecialistRequest(
            intent=Intent(action=action, domain=domain, confidence=1.0, parameters=parameters),
            query=Query(user_input=query),
            specialist_name=self.manifest.name,
        )
        response = await self._specialist.execute(request)
        return {{
            "status": response.status,
            "specialist_name": response.specialist_name,
            "result": response.result,
        }}

    def describe(self) -> dict[str, Any]:
        """Return the skill manifest as a plain dict (for registration)."""
        return {{
            "name": self.manifest.name,
            "display_name": self.manifest.display_name,
            "description": self.manifest.description,
            "version": self.manifest.version,
            "capabilities": self.manifest.capabilities,
            "input_schema": self.manifest.input_schema,
        }}
'''


