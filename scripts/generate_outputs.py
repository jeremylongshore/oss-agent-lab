def main() -> None:
    parser = argparse.ArgumentParser(
        description="Generate multi-format output wrappers from a specialist directory.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__,
    )
    parser.add_argument(
        "specialist_dir",
        type=Path,
        help="Path to specialist directory (must contain SKILL.md and agent.py).",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=None,
        metavar="DIR",
        help="Root directory for generated files (default: <project_root>/output).",
    )
    parser.add_argument(
        "--list",
        action="store_true",
        help="Print generated file paths after writing.",
    )
    args = parser.parse_args()

    try:
        generated = generate_outputs(args.specialist_dir, args.output_dir)
    except (FileNotFoundError, ValueError) as exc:
        print(f"ERROR: {exc}", file=sys.stderr)
        sys.exit(1)

    for fmt, path in generated.items():
        print(f"{fmt:15s} {path}")


if __name__ == "__main__":
    main()
